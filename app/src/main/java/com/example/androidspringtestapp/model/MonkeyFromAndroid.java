package com.example.androidspringtestapp.model;

public class MonkeyFromAndroid {
    String name;
    int age;

    public MonkeyFromAndroid(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
